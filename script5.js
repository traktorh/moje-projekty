let score = 0;
let answeredQuestions = 0;
const totalQuestions = quizData.length;
const quizContainer = document.getElementById('quiz-questions');
const resultContainer = document.getElementById('result-container');

// Funkcja generująca pytania w HTML
function loadQuiz() {
    quizData.forEach((item, index) => {
        const questionElement = document.createElement('div');
        questionElement.classList.add('question');
        questionElement.id = `q${index}`;

        let optionsHtml = item.options.map(option => `
            <label>
                <input type="radio" name="q${index}_answer" value="${option}">
                ${option}
            </label>
        `).join('');

        questionElement.innerHTML = `
            <p>${item.question}</p>
            <div class="options-group">
                ${optionsHtml}
            </div>
            <p class="feedback" id="feedback_q${index}"></p>
        `;

        quizContainer.appendChild(questionElement);
    });
}

// Funkcja sprawdzająca wszystkie odpowiedzi po kliknięciu przycisku
function submitQuiz() {
    score = 0;
    answeredQuestions = 0;

    quizData.forEach((item, index) => {
        const questionId = `q${index}`;
        const radios = document.getElementsByName(`${questionId}_answer`);
        const feedbackElement = document.getElementById(`feedback_${questionId}`);
        
        let selectedAnswer = null;
        
        // Znajdź wybraną odpowiedź
        for (const radio of radios) {
            if (radio.checked) {
                selectedAnswer = radio.value;
                answeredQuestions++;
                break;
            }
        }

        // Resetowanie stylów i tekstu
        feedbackElement.className = 'feedback';
        feedbackElement.textContent = '';

        if (selectedAnswer) {
            // Sprawdzenie poprawności
            if (selectedAnswer === item.answer) {
                score++;
                feedbackElement.textContent = '✅ Верно нахуй!';
                feedbackElement.classList.add('correct');
            } else {
                feedbackElement.textContent = `❌ Неверно нахуй.Правильный ответ: ${item.answer}`;
                feedbackElement.classList.add('incorrect');
            }
        } else {
             // Opcjonalnie: Oznaczanie pytań bez odpowiedzi
             feedbackElement.textContent = 'Где сука ответ.';
             feedbackElement.classList.add('missing');
        }
        feedbackElement.style.display = 'block';
    });

    // Wyświetlenie końcowego wyniku
    resultContainer.innerHTML = `
        <h2>Твой IQ: ${score} / ${totalQuestions}</h2>
        <p>Обротил внимание на ебаные ${answeredQuestions} из ${totalQuestions} вопросов.</p>
    `;
    
    // Przewinięcie do góry do wyniku
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

// Uruchomienie ładowania quizu po załadowaniu strony
window.onload = loadQuiz;